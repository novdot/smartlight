void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin== GPIO_PINx_BTN) {
		//Защита от дребезга. Если прерывание прилетело слишком быстро - просто проигнорируем
		if(abs(HAL_GetTick()-g_start)>500) {
			g_start = HAL_GetTick();
		} else
			return;
		//Если все нормально - переключим режим работы
		smartlight_control_SwitchWorkmode();
	} else {
		__NOP();
	}
}

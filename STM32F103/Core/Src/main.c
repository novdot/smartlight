void adc_read(void)
{
	HAL_ADC_Start(&HADCx);

	HAL_ADC_PollForConversion(&HADCx,100);
	g_Setup.sensors.nPotentiometr = HAL_ADC_GetValue(&HADCx);

	HAL_ADC_PollForConversion(&HADCx,100);
	g_Setup.sensors.nPhoto = HAL_ADC_GetValue(&HADCx);

	HAL_ADC_Stop(&HADCx);
}

